ICON_GROUPS = {
    'trade_buy': ('arrow_up_right', '#FF7A29'),  # 买入 - 橙色
    'trade_sell': ('arrow_down_right', '#7B8B9A'),  # 卖出 - 灰青
    'positive': ('trending_up', '#FF7A29'),  # 上涨
    'negative': ('trending_down', '#7B8B9A'),  # 下跌
    'success': ('check_circle', '#4CAF50'),  # 成功
    'warning': ('alert_triangle', '#FFA726'),  # 警告
    'info': ('info', '#42A5F5'),  # 信息
}


def get_icon_group(group_name: str, size: int = 20) -> str:
    """获取预定义的图标组合"""
    if group_name in ICON_GROUPS:
        name, color = ICON_GROUPS[group_name]
        return icon(name, size, color)
    return icon('info', size)
