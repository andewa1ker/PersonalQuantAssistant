def icon(name: str, size: int = 20, color: str = 'currentColor') -> str:
    """
    获取SVG图标
    
    参数:
        name: 图标名称（来自ICONS字典）
        size: 图标大小（像素）
        color: 图标颜色（CSS颜色值或currentColor）
    
    返回:
        格式化的SVG字符串
    """
    if name not in ICONS:
        return f'<!-- Icon "{name}" not found -->'
    
    return ICONS[name].format(size=size, color=color)


def icon_html(name: str, size: int = 20, color: str = 'currentColor', css_class: str = '') -> str:
    """
    获取包装在span中的SVG图标（方便在Streamlit中使用）
    
    参数:
        name: 图标名称
        size: 图标大小
        color: 图标颜色
        css_class: 额外的CSS类名
    
    返回:
        HTML字符串
    """
    svg = icon(name, size, color)
    return f'<span class="icon {css_class}" style="display:inline-flex;align-items:center;justify-content:center;vertical-align:middle;">{svg}</span>'


